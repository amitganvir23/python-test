#!/usr/bin/python
a = " Hello, World!"
x = "a,b,c,d"
print (a) # Print all string value
print (a[1]) # Get the character at position 1
print (a[1:6]) # Get the characters from position 1 to position 6
print (len(a))# The len() method returns the length of a string: (Including white space)
print (a.strip()) # The strip() method removes any whitespace from the beginning or the end:
print (a.upper()) # The upper() Translate string value to Upper case, like 'tr' commmand
print (a.lower()) # The lower() Translate string value to Lower case, like 'tr' commmand
print (a.replace("o", "O")) # The replace() method replaces a string with another string:
print (x.split(","))
